package fr.eql.ai111.java.init.demo.keyword;

public class DemoStaticFinal {

    /*
    Un attribut statique et final est en général appelé
    une constante.
    Une constante s'écrit en majuscules, les mots séparés par des
    underscores (tirets bas)
     */
    private static final int POSITIVE_NUMBER = 5;

    public static void main(String[] args) {
        System.out.println(POSITIVE_NUMBER);
    }
}
